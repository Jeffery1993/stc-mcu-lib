#include "delay.h"

//==================================================
// 函数功能：延时x毫秒
// 输入参数：整型x
// 输出参数：无
//==================================================
void delay_ms(uint x)
{
	uint i,j;
	for(i=x;i>0;i--)
		for(j=114;j>0;j--);
}

//==================================================
// 函数功能：延时x秒
// 输入参数：整型x
// 输出参数：无
//==================================================
void delay_s(uint x)
{
	uint i,j,k;
	for(i=x;i>0;i--)
		for(j=1000;j>0;j++)
			for(k=114;k>0;k--);
}